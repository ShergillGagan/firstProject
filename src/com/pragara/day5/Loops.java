package com.pragara.day5;

public class Loops {
    public static void main(String[] args) {
        int a = 9;
        int b = ++a;  // pre increment  by 1
         int c = a++; // post increment by 1

        System.out.println(b);
        System.out.println(c);
        System.out.println(a);
    }



}
